/**
 * Pin Configuration for ESP32-S3-Touch-AMOLED-1.8 (Waveshare)
 */

#ifndef PIN_CONFIG_H
#define PIN_CONFIG_H

// ═══════════════════════════════════════════════════════════════════════════════
//  DISPLAY (QSPI)
// ═══════════════════════════════════════════════════════════════════════════════
#define LCD_SDIO0  4
#define LCD_SDIO1  5
#define LCD_SDIO2  6
#define LCD_SDIO3  7
#define LCD_SCLK   11
#define LCD_CS     12
#define LCD_WIDTH  368
#define LCD_HEIGHT 448

// ═══════════════════════════════════════════════════════════════════════════════
//  I2C BUS
// ═══════════════════════════════════════════════════════════════════════════════
#define IIC_SDA    15
#define IIC_SCL    14

// ═══════════════════════════════════════════════════════════════════════════════
//  TOUCH
// ═══════════════════════════════════════════════════════════════════════════════
#define TP_INT     38
#define TP_RESET   9

// ═══════════════════════════════════════════════════════════════════════════════
//  I2C ADDRESSES
// ═══════════════════════════════════════════════════════════════════════════════
#define EXPANDER_ADDR       0x20
#define AXP2101_SLAVE_ADDRESS 0x34
#define FT3168_ADDR         0x38
#define RTC_ADDR            0x51
#define QMI8658_ADDR        0x6B

// ═══════════════════════════════════════════════════════════════════════════════
//  SD CARD (SDMMC 1-bit)
// ═══════════════════════════════════════════════════════════════════════════════
#define SDMMC_CLK  2
#define SDMMC_CMD  1
#define SDMMC_DATA 3

// ═══════════════════════════════════════════════════════════════════════════════
//  AUDIO
// ═══════════════════════════════════════════════════════════════════════════════
#define I2S_MCK    16
#define I2S_BCK    9
#define I2S_WS     45
#define I2S_DO     10
#define I2S_DI     8
#define PA_PIN     46

// ═══════════════════════════════════════════════════════════════════════════════
//  BUTTONS
// ═══════════════════════════════════════════════════════════════════════════════
#define BTN_BOOT   0

#endif
